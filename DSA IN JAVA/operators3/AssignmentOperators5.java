package operators3;

public class AssignmentOperators5 {
	public static void main(String[] args) {
		
		//Equal to(=) - assign the value right to left
		
//		int B = 10;
//		int A = B;
//		System.out.println(A);	//10
		
		int A = 10;
		int B = 5;
		A =  B;
//		System.out.println(A);	//5
		
		
		//Plus equal to (+=)
		
		int C = 5;
//		C = C + 10;	//15
		C += 10;	//15
//		System.out.println(C);	//15
		
		
		//Minus equal to (-=)
		int D = 10;
//		D = D - 5;	//5
		D -= 5;		//5
//		System.out.println(D);	//5
		
		
		//Multiply equal to (*=)
		int E = 10;
//		E = E * 5;	//50
		E *= 5;		//50
//		System.out.println(E);	//50
		
		
		//Divide equal to (/=)
		int F = 10;
//		F = F / 2;	//5
		F /= 2;		//5
//		System.out.println(F);	//5
		
		
		//Modulo equal to (%=)
		int G = 10;
//		G = G % 3;	//1
		G %= 3;		//1
//		System.out.println(G);	//1
		
		System.out.println(3/3);
		
	}
}
